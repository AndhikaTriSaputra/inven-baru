        // Auto-print when page loads - multiple approaches for better compatibility
        let printAttempted = false;
        
        function attemptPrint() {
            if (printAttempted) return;
            printAttempted = true;
            
            try {
                window.print();
            } catch (e) {
                console.log('Print failed:', e);
            }
        }
        
        // Try immediate print first
        document.addEventListener('DOMContentLoaded', function() {
            setTimeout(attemptPrint, 500);
        });
        
        // Fallback for window.onload
        window.onload = function() {
            setTimeout(attemptPrint, 1000);
        };
        
        // Additional fallback for older browsers
        window.addEventListener('load', function() {
            setTimeout(attemptPrint, 1500);
        });
        
        // Force print on any user interaction (click, keypress, etc.)
        document.addEventListener('click', function() {
            attemptPrint();
        });
        
